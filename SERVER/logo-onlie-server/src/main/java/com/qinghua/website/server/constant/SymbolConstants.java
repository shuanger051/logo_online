package com.qinghua.website.server.constant;

/**
 * 常用的符号常量类型,方便使用
 */
public interface SymbolConstants {

    /**
     * 逗号
     */
    String COMMA = ",";

    /**
     * 点
     */
    String SPOT = ".";

    /**
     * 两点 一般用于路径表示
     */
    String SPOT_TWO = "..";

    /**
     * 井号
     */
    String POUND = "#";

    /**
     * 冒号
     */
    String COLON = ":";

    /**
     * 下划线
     */
    String UNDERLINE = "_";

    /**
     * 斜杠  一般用于路径表示
     */
    String SLASH = "/";

}
